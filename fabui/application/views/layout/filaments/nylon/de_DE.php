<p>Wenn 3D-Druck gestartet wurde, wurde dieses Material nicht unter den
    Extrudierbare filamente. Es ist flexibel und widerstandsfähig gegen Traktionen.<br>
	Es ist bekannt für seine Verwendung in Textil, aber auch in Branchen, 
	die ein starkes und flexibles Material erfordert.<br>Es gibt verschiedene Arten von
	Nylon: 3D-Druck verwendet meist Nylon 6 und Nylon 6.6, die die sind
	am gebräuchlichsten. <br>Es erfordert höhere Temperaturen um gedruckt zu werden. 
	Also ein 3D Drucker muss in der Lage sein diese zu erreichen (zirka <strong>240&deg;C</strong>): Natürlich das FABtotum kann.
	
<p>
<p>Seine wichtigsten technischen Spezifikationen sind:</p>
<ul>
	<li>Es ist stark und flexibel, mehr als andere druckbare Materialien;</li>
	<li>Seine endgültige Fertigstellung ist nicht perfekt (im Durchschnitt ist es immer noch genug)</li>
	<li>Es ist wiederverwendbar.</li>
</ul>
<p>
	<strong>Tipps</strong>
</p>
<ul>
	<li>Beheiztes Bett ist nicht nötig: es ist möglich, es kalt zu lassen;</li>
	<li>Wenn es nicht richtig oder mit Blasen extrudiert, kann die Temperatur zu hoch sein;</li>
	<li>Nylon, das normalerweise in transparenten Spulen kommt, kann gefärbt werden;</li>
	<li>Wählen Sie immer hochwertige Filamente wie die von FABtotum.</li>
</ul>
