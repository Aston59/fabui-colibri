<p>When 3D printing started this material was not listed among the
	extrudable filaments. It is flexible as well as resistant to tractions.<br>
	It is well known for its uses in textile but also in industries which
	requires a strong and flexible material.<br>There are different kinds of
	Nylon: 3D printing mostly uses Nylon 6 and Nylon 6.6, which are the
	most common. <br>It requires higher temperatures to be printed, so a 3D
	printer must be able to reach them (around <strong>240&deg;C</strong>): the FABtotum, of
	course, can.
<p>
<p>Its major technical specs are:</p>
<ul>
	<li>it is strong and flexible, more than other printable materials;</li>
	<li>its final finishing is not perfect (on average it is still enough
		though);</li>
	<li>it is reusable.</li>
</ul>
<p>
	<strong>Tips</strong>
</p>
<ul>
	<li>heated bed is not needed: it is possible to leave it cold;</li>
	<li>if it is not extruding correctly or with bubbles, temperature might
		be too high;</li>
	<li>Nylon, which normally comes in transparent spools, can be coloured;</li>
	<li>always choose high quality filaments like FABtotum's ones.</li>
</ul>