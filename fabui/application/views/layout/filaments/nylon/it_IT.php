<p> Il <strong>nylon</strong> &egrave; un materiale
	flessibile e resistente alla trazione ed &egrave; particolarmente conosciuto
	per l'utilizzo che ne viene fatto nell'industria tessile ma anche nelle
	industrie che necessitano di un materiale resistente e flessibile. <br>Ci
	sono molte tipologie diverse di nylon: la stampa 3D utilizza
	principalmente i nylon 6 e 6.6, che sono quelli pi&ugrave; comuni.<br> Questo
	materiale necessita di alte temperature per essere stampato ( <strong>240&deg;</strong> circa).</p>

<p>Le principali caratteristiche tecniche sono:</p>
<ul>
	<li>&egrave; resistente e flessibile, pi&ugrave; di altri materiali stampabili;</li>
	<li>la finitura finale non &egrave; perfetta (ma sufficiente per la maggior
		parte delle lavorazioni richieste);</li>
</ul>

<p>
	<strong>Consigli</strong>
</p>

<ul>
	<li>il piatto di stampa riscaldato non &egrave; necessario: &egrave; possibile
		lasciarlo freddo;</li>
	<li>se l'estrusione non avviene correttamente o contiene bolle, la
		temperatura di stampa potrebbe essere troppo alta;</li>
	<li>il nylon, che normalmente &egrave; composto da bobine trasparenti, pu&ograve;
		essere acquistato in diversi colorazioni;</li>
	<li>utilizza sempre filamenti in nylon di alta qualit&agrave; come quelli di
		FABtotum.</li>
</ul>