<p>
	Dieses Material ist einfach zu extrudieren und ist eines der am meisten 
	verwendeten in 3D-Druck, um sehr gut fertige Objekte zu bekommen.<br>
	It is not suistainable and its smoke can be dangerous if inhalated.<br>
	Der Grund, diesen Faden zu PLA zu bevorzugen, ist vor allem wegen seiner Präzision und mechanischen
Spezifikationen.</p>
	<p><strong>ABS</strong> (Für Kunststoff) steht für Acrylnitril-Butadien-Styrol und
	Es ist eine thermoplastisk die in alltäglichen Gegenständen weit verbreitet ist. <br>
	Es kann mit jedem FFF 3D Drucker bedruckt werden, der zu hohen Temperaturen kommen kann,
	Da es in einem Bereich zwischen <strong>220&deg;</strong> und <strong>245&deg;</strong> FABtotum kann es probleme loss machen</p>
<p>Seine wichtigsten technischen Spezifikationen sind:</p>
<ul>
	<li>Es ist sehr hart und widersteht mehr zu Verwerfungen und Schäden;</li>
	<li>Es verformt sich nicht leicht;</li>
	<li>Es ist langlebig in der Leistung und weniger unterliegen sich zu verschlechtern wenn es ausgesetzt ist (zu Licht, Feuchtigkeit und so weiter).</li>
</ul>
<p>
	<strong>Tipps</strong>
</p>
<ul>
	<li>ABS-Filament wenn extrudiert neigt dazu sich zu locken: Verwenden Sie eine beheizte
Plattform wie FABtotums Hybridbett mindestens bei <strong> 60&deg;/70&deg;</strong> zu vermeiden
Dies. Wenden Sie eine kleine Menge Haarspray an, um das Objekt auf die Oberfläche zu kleben.</li>
	
	<li>Ventilator sollte ausgeschaltet sein, um das kalte Ende des Druckkopfes zu vermeiden, um zu wärmen;</li>
	
	<li>ABS kann rauchig und stinkend sein: Verwenden Sie es in einem gut belüfteten Bereich / Raum;</li>
	<li>Wählen Sie immer hochwertige Filamente wie die von FABtotum.</li>

</ul>
